import random
print(random.randint(5, 20))  # line 1
# Answer was 16. The smallest number would've been 5 and the largest 20
print(random.randrange(3, 10, 2))  # line 2
# Answer was 3, the smallest number from 3 to 9. This couldn't have been 4
print(random.uniform(2.5, 5.5))  # line 3
# Answer was 3.130619543917724. The smallest number would have been 2.5, the largest 5.5
print(random.randint(1, 100))
